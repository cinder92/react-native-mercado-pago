import Foundation

internal extension Int {
    internal var stringValue: String {
        return "\(self)"
    }
}

internal extension Int64 {
    internal var stringValue: String {
        return "\(self)"
    }
}

internal extension Double {
    internal var stringValue: String {
        return "\(self)"
    }
}
